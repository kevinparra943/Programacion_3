    private void agregarTarea(String tipo, int duracion) {
        Tarea tarea = new Tarea(tipo, duracion);
        pilaTareas.agregarTarea(tarea);
        
        if (pilaTareas.getEnEjecucion() == tarea) {
        modelo1.insertRow(0, new Object[]{tarea.getTipo()});
    } else {
        modelo1.insertRow(1, new Object[]{tarea.getTipo()});
    }
       

        totalDuracion += duracion;
        jProgressBar1.setMaximum(totalDuracion);

        if (!tiempo.isRunning()) tiempo.start();
    }
