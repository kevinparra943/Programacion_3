    Timer tiempo = new Timer (500, new ActionListener ()
 {
          public void actionPerformed(ActionEvent e){
              metodo();
          } 
     });

    public void metodo(){
             if (pilaTareas.vacia()) {
            tiempo.stop();
            jLabel2.setText("Sin tareas pendientes");
            return;
        }

        pilaTareas.avanzar();
        Tarea actual = pilaTareas.getEnEjecucion();
        if (actual != null) {
            duracionEjecutada += 500;
            jProgressBar1.setMaximum(totalDuracion);
            jProgressBar1.setValue(duracionEjecutada);
            //jProgressBar1.setString("Total: " + duracionEjecutada + "/" + totalDuracion + " ms");
            jLabel2.setText("Procesando: " + actual.getTipo());
        } else {
            // terminó una tarea, quitar de la tabla
            if (modelo1.getRowCount() > 0) {
                modelo1.removeRow(0);
            }
        }
}
    

